package com.qa.java.oop.features;

public class CarProp {
	int id;
	String name;
	float price;
	String engineCC;
	String fuelType;
	int manifacturingYear;


	public CarProp(int id, String name, float price, String engineCC, String fuelType, int manifacturingYear) {
		super();
		this.id = id;
		this.name = name;
		this.price = price;
		this.engineCC = engineCC;
		this.fuelType = fuelType;
		this.manifacturingYear = manifacturingYear;
	}
}
